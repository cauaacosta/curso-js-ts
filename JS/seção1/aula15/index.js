//Numbers em JS
// IEEE 754-2008 padrão do JS para números
let num1 = 1;
let num2 = 2.5;

console.log(num1.toString()); // converte o valor para string
console.log(num1.toString(2)); // mostra a representação binária desse número
console.log(num1.toFixed(2)); // mostra a quantidade de casas decimais definidas no parenteses
console.log(Number.isInteger(num1)); //mostra se é um número inteiro (true/false)

